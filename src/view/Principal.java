    public void initAll() {
        controlador  = new ProductosController(); // Inicializa el Controlador y Conexion
         
        allProducts = controlador.getAllProducts(); 
        
        if ((allProducts.isEmpty()) | (allProducts == null)) {
            
            controlador.createTableProductos();
            
        } else {
           limpiarTabla();
           loadAllProducts();
        }
    
    }
    
    //Carga todos los componentes
    private void loadAllProducts(){
         int c = 0;
            while (c != allProducts.size()) {
             //list1.add(allProducts.get(c).nombre);
             ModelProductos get = allProducts.get(c);
                jTable1.setValueAt( get.getIdProducto(), c, 0);
                jTable1.setValueAt( get.getNombre(), c, 1);
                jTable1.setValueAt( get.getCantidad(), c, 2);
                jTable1.setValueAt( get.getCantidad_minima(), c, 3);

                jTable1.setValueAt( get.getUsuario_registra(), c, 4);
                jTable1.setValueAt( get.getUsuario_actualiza(), c, 5);
                c++;
            }
    }
    
    //Limpia la tabla
    private void limpiarTabla (){
        
        int c = 0;
        int k = 0;
        
        while(c!= jTable1.getColumnCount()){
            
            k = 0;
            while(k!= jTable1.getRowCount()){
                jTable1.setValueAt("", k, c);
                k = k+1;
            }
            c = c+1;
        }
    }
    
    //Limpia la tabla
    private void cargarTabla (ArrayList<ModelProductos> array){
        
        int c = 0;
       
        try{
        while(c!= jTable1.getColumnCount()){
            
            ModelProductos get = array.get(c);
            jTable1.setValueAt(get.getIdProducto(), c, 0);
            
            jTable1.setValueAt(get.getNombre(), c, 1);
            
            jTable1.setValueAt(get.getCantidad(), c, 2);
            
            jTable1.setValueAt(get.getCantidad_minima(), c, 3);
            
            jTable1.setValueAt(get.getUsuario_registra(), c, 4);
            
            jTable1.setValueAt(get.getUsuario_actualiza(), c, 5);
            
            c = c+1;
        }}
        catch(Exception e){
            
        }
    }
    
  private void imprimir() {
       
        JFileChooser file = new JFileChooser();

        FileNameExtensionFilter xmlfilter = new FileNameExtensionFilter(
                "pdf files (*.pdf)", "pdf");
        file.setFileFilter(xmlfilter);
        file.setDialogTitle("Open schedule file");
// set selected filter
        file.setFileFilter(xmlfilter);
        
        
        
        file.showOpenDialog(this);
        File abre = file.getSelectedFile();
        if (abre != null) {
            
            boolean path = abre.getAbsolutePath().contains(".pdf");
            if (path) {
                new PrintReport(abre.getAbsolutePath(),jTable1);
            } else {
                new PrintReport(abre.getAbsolutePath()+".pdf",jTable1);
            }
        }

}   
